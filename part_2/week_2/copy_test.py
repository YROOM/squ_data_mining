# 尝试3：将切片中的元素指向新对象（会影响原列表）
lst_slice[4][0] = 789  
print(f"切片再次修改: {lst_slice}")  # [0, 3, 4, 5, [789, 456]]
print(f"原列表: {lst}")  # [1, 2, 3, 4, 5, [789, 456], 7] - 变！