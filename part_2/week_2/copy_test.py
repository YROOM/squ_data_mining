lst = [1,2,3,4,5,[123,456],7]
print(f"原始列表: {lst}")

lst_slice = lst[1:6]
print(f"切片: {lst_slice}")

# 尝试1：修改切片中的不可变元素（不会影响原列表）
lst_slice[0] = 0
print(f"切片修改后: {lst_slice}")  # [0, 3, 4, 5, [123, 456]]
print(f"原列表: {lst}")  # [1, 2, 3, 4, 5, [123, 456], 7] - 没变！

