# 转换为NumPy数组（方便按条件筛选）
students_np = np.array(students, dtype=[
    ('student_id', 'U20'),  # 学号：字符串类型
    ('name', 'U20'),  # 姓名：字符串类型
    ('gender', 'U2'),  # 性别：字符串类型
    ('class', 'U10')  # 班级：字符串类型
])


def random_call(seed=None, count=1, gender=None):
    """
    NumPy随机点名器
    :param seed: 随机种子（None则每次随机，指定整数则结果可复现）
    :param count: 点名人数（默认1人）
    :param gender: 筛选性别（None=全部，'男'/'女'=指定性别）
    :return: 被点到的学生信息列表
    """
    # 1. 初始化NumPy随机数生成器
    rng = np.random.default_rng(seed=seed)

    # 2. 按性别筛选学生
    if gender is not None:
        filtered_students = students_np[students_np['gender'] == gender]
        if len(filtered_students) == 0:
            print(f"错误：未找到{gender}性学生！")
            return []
    else:
        filtered_students = students_np

    # 3. 生成随机索引（基于筛选后的学生总数）
    total = len(filtered_students)
    # 确保不重复点名（count<=total时用choice，否则允许重复）
    if count <= total:
        random_indices = rng.choice(total, size=count, replace=False)
    else:
        random_indices = rng.choice(total, size=count, replace=True)
        print(f"提示：点名人数({count})超过学生总数({total})，允许重复点名")

    # 4. 获取被点到的学生信息
    called_students = filtered_students[random_indices]

    # 5. 打印结果（课堂展示用）
    print("=" * 50)
    print(f"🎯 随机点名结果（共{count}人）：")
    print("=" * 50)
    for i, student in enumerate(called_students, 1):
        print(
            f"{i}. 学号：{student['student_id']} | 姓名：{student['name']} | 性别：{student['gender']} | 班级：{student['class']}")
    print("=" * 50)

    return called_students


