# 2. 索引数组
# python
# [[1, 2],
#  [2, 3]]
# 形状为(2, 2)，表示我们要得到一个2×2的结果，但每个元素都是一整列（从每行中取对应列的值）。
#
# 3. 取值过程
# 对于每一行，我们都要按照索引数组选取对应的列：
#
# 第0行 [0, 5, 10, 15]：
#
# 索引1 → 取列1: 5
#
# 索引2 → 取列2: 10
#
# 索引2 → 取列2: 10
#
# 索引3 → 取列3: 15
# 得到：[[5, 10], [10, 15]]
#
# 第1行 [20, 25, 30, 35]：
#
# 索引1 → 取列1: 25
#
# 索引2 → 取列2: 30
#
# 索引2 → 取列2: 30
#
# 索引3 → 取列3: 35
# 得到：[[25, 30], [30, 35]]
#
# 第2行 [40, 45, 50, 55]：
#
# 索引1 → 取列1: 45
#
# 索引2 → 取列2: 50
#
# 索引2 → 取列2: 50
#
# 索引3 → 取列3: 55
# 得到：[[45, 50], [50, 55]]
#
# 第3行 [60, 65, 70, 75]：
#
# 索引1 → 取列1: 65
#
# 索引2 → 取列2: 70
#
# 索引2 → 取列2: 70
#
# 索引3 → 取列3: 75
# 得到：[[65, 70], [70, 75]]
#
